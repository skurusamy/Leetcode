import random
def findMatch(word1, word2):
    count = 0
    for i in range(len(word1)):
        if word1[i] == word2[i]:
            count += 1
    return count
def getRandomWord(wordlist):
    index = random.randint(0,len(wordlist)-1)
    return wordlist[index]
def removeWords(wordlist,match, word):
    if match == -1:
        return wordlist.remove(word)
    for w in wordlist:
        if findMatch(w,word) != match:
            wordlist.remove(w)
    return wordlist

